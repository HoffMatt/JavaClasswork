package Class3;

public class ClassTask1 {
    public static void main(String[] args) {
        double num1=22.99;
        double num2=44.45;
        double add=(num1+num2);
        double subt=(num1-num2);
        double multi=(num1*num2);
        double div=(num1/num2);
        System.out.println("The addition of two numbers "+num1+ " and "+ num2+ " is equal to "+add);
        System.out.println("The subtraction of two numbers "+num1+ " and "+ num2+ " is equal to "+subt);
        System.out.println("The multiplication of two numbers "+num1+ " and "+ num2+ " is equal to "+multi);
        System.out.println("The division of two numbers "+num1+ " and "+ num2+ " is equal to "+div);
    }
}